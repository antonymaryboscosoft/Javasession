package OOPS;

public class OOPSFeature {

	// 1.Inheritance
	
		/*When one object acquires all the properties and behaviors of a parent object,
		it is known as inheritance. It provides code reusability. It is used to achieve 
		runtime polymorphism.*/

	//2.Polymorphism
		/*if one task is performed by different ways, it is known as polymorphism. 
		For example: to convince the customer differently, to draw something, for example, 
		shape, triangle, rectangle, etc.
	   */

	//3.Abstraction.
		/*Hiding internal details and showing functionality is known as abstraction. 
		For example phone call, we don't know the internal processing.
	   */
	//4.Encapsulation
		/*Binding (or wrapping) code and data together into a single unit are known as 
		encapsulation. For example capsule, it is wrapped with different medicines.*/
}
